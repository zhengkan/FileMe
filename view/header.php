1. 什么是File Me？
    向我发文件吧！！！
    使用本平台，你将开放个人云空间给他人，供他人上传文件到你的个人云空间。

2. 什么场景会File Me平台？
    典型场景：
        大学老师收各个班级各个同学的课程作业。
    在没File Me前怎么做： 
        每个同学都给老师发邮件； 老师收一大堆邮件，极难整理；
    在拥有File Me怎么做？
        step1. 为每个班级开启一个上传任务，以班级名称为上传任务名；
                此时File Me在你个人云空间中，为每个上传任务创建一个单独的目录，并给每个任务分配全局唯一上传链接；
        step2. 老师，将各上传链接分给不同的班级；
        step3. 每个同学自行上传，文件完美整理好了！！！！  亲！！！！ 

